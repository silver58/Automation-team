const SALCOMP_LANG_CYCLE = ['en', 'es', 'vi'];

window.SALCOMP_LANG = (() => {
  try {
    const saved = localStorage.getItem('salcomp_lang');
    return SALCOMP_LANG_CYCLE.includes(saved) ? saved : 'en';
  } catch(e) { return 'en'; }
})();

window.t = function(key, fallback) {
  const entry = window.SALCOMP_I18N[key];
  if (!entry) return fallback || key;
  return entry[window.SALCOMP_LANG] || entry.en || fallback || key;
};

window.applyTranslations = function() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const txt = window.t(key);
    if (el.children.length === 0) {
      el.textContent = txt;
    } else {
      for (const node of el.childNodes) {
        if (node.nodeType === 3 && node.textContent.trim()) {
          node.textContent = txt;
          return;
        }
      }
      el.insertBefore(document.createTextNode(txt), el.firstChild);
    }
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    el.placeholder = window.t(el.getAttribute('data-i18n-placeholder'));
  });
  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    el.title = window.t(el.getAttribute('data-i18n-title'));
  });
};

function syncToggleVisuals() {
  document.querySelectorAll('.lang-toggle').forEach(btn => {
    btn.querySelector('.lang-en')?.classList.toggle('active', window.SALCOMP_LANG === 'en');
    btn.querySelector('.lang-es')?.classList.toggle('active', window.SALCOMP_LANG === 'es');
    btn.querySelector('.lang-vi')?.classList.toggle('active', window.SALCOMP_LANG === 'vi');
  });
}

// Auto-inject the VI label into existing toggles so older HTML still works
function injectViLabel() {
  document.querySelectorAll('.lang-toggle').forEach(btn => {
    if (!btn.querySelector('.lang-vi')) {
      const span = document.createElement('span');
      span.className = 'lang-vi';
      span.textContent = 'VI';
      if (window.SALCOMP_LANG === 'vi') span.classList.add('active');
      btn.appendChild(span);
    }
  });
}

window.toggleLanguage = function() {
  // Cycle EN → ES → VI → EN
  const cur = SALCOMP_LANG_CYCLE.indexOf(window.SALCOMP_LANG);
  const next = SALCOMP_LANG_CYCLE[(cur + 1) % SALCOMP_LANG_CYCLE.length];
  window.SALCOMP_LANG = next;
  try { localStorage.setItem('salcomp_lang', window.SALCOMP_LANG); } catch(e) {}
  syncToggleVisuals();
  window.applyTranslations();
  if (typeof window.onLangChange === 'function') window.onLangChange();
  document.documentElement.lang = window.SALCOMP_LANG;
};

if (document.readyState === 'loading') {
  document.addEventListener('DOMContentLoaded', () => {
    injectViLabel();
    window.applyTranslations();
    syncToggleVisuals();
  });
} else {
  injectViLabel();
  window.applyTranslations();
  syncToggleVisuals();
}
document.documentElement.lang = window.SALCOMP_LANG;
